			but following Apple conventions is probably the best move.</li>
		<li>By default, Internet Explorer 11 looks for <code>browserconfig.xml</code> at the root of the web site.</li>
		<li>Several services, such as <a href="http://help.yandex.com/webmaster/search-results/favicon.xml#how-to-create">Yandex</a>,
			look for <code>favicon.ico</code> in the root directory.</li>
